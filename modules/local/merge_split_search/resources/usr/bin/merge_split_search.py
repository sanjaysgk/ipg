import subprocess
import sys
import typing

import numpy as np


# ---------------------------------------------------------------------------
# Named tuples (from msfragger_pep_split.py lines 244-246)
# ---------------------------------------------------------------------------

specinfo = collections.namedtuple(
    "specinfo", ["massdiff", "hyperscore", "nextscore", "expectscore", "part", "xml"]
)

Prot = collections.namedtuple(
    "Prot",
    ["protein_descr", "protein", "peptide_prev_aa", "peptide_next_aa", "num_tol_term"],
)


# ---------------------------------------------------------------------------
# Regex patterns (from msfragger_pep_split.py lines 197-242, 290, 335-340)
# These parse pepXML/PIN format and must match exactly.
# ---------------------------------------------------------------------------

re_search_hit_first_line = re.compile(
    rb'(?<=hit_rank=")(\d+)(?=" )' b"|" b'(?<=protein=")(.+?)(?=" )'
)

re_search_hit = re.compile(b"^(<search_hit.+?^</search_hit>)", re.DOTALL | re.MULTILINE)

re_scores = re.compile(
    b'^<search_score name="hyperscore" value="(.+?)"/>\n'
    b'<search_score name="nextscore" value="(.+?)"/>\n',
    re.MULTILINE,
)

re_massdiff_scores = re.compile(
    b'<search_hit .*? massdiff="(.+?)".*(?s:.*?)\n'
    b'<search_score name="hyperscore" value="(.+?)"/>\n'
    b'<search_score name="nextscore" value="(.+?)"/>\n'
)

re_update_search_hit = re.compile(
    rb"""\A(.+hit_rank=")(?:.+?)("(?s:.+?))
<search_score name="hyperscore" value="(?:.+?)"/>
<search_score name="nextscore" value="(?:.+?)"/>
<search_score name="expect" value="(?:.+?)"/>
((?s:.+))\Z"""
)

re_pepxml_header = re.compile(b"""(.+?)^</search_summary>""", re.DOTALL | re.MULTILINE)

sh_peptide = re.compile(b' peptide="(.+?)"')
sh_protein_descr = re.compile(b'protein_descr="(.*?)"')
sh_protein = re.compile(b'protein="(.+?)"')
sh_peptide_prev_aa = re.compile(b'peptide_prev_aa="(.+?)"')
sh_peptide_next_aa = re.compile(b'peptide_next_aa="(.+?)"')
sh_num_tol_term = re.compile(b'num_tol_term="(.+?)"')


