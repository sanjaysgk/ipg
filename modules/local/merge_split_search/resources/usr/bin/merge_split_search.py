def main() -> None:
    """
    Main entry point for merge_split_search.

    Orchestrates the full merge workflow:
    1. Sum score histograms across chunks
    2. Generate expect functions via MSFragger
    3. Merge pepXML and PIN files per sample
    """
    if sys.version_info[:2] >= (3, 7):
        sys.stdout.reconfigure(encoding="utf-8")
        sys.stderr.reconfigure(encoding="utf-8")

    args = parse_args()
    outdir = pathlib.Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    # Resolve chunk directories
    if args.chunk_dirs:
        chunk_dirs = [pathlib.Path(d) for d in args.chunk_dirs.split(",")]
    else:
        raise RuntimeError("--chunk_dirs is required")

    assert len(chunk_dirs) == args.num_chunks, (
        f"Expected {args.num_chunks} chunk dirs, got {len(chunk_dirs)}"
    )

    print(
        f"Merging split search results for sample '{args.sample_name}' "
        f"from {args.num_chunks} chunks",
        flush=True,
    )

    # Step 1: Sum score histograms across chunks
    print("Step 1: Combining score histograms...", flush=True)
    combined_histo = write_combined_scores_histo(args.sample_name, chunk_dirs, outdir)
    print(f"  Combined histogram: {combined_histo}", flush=True)

    # Step 2: Generate expect functions using MSFragger
    print("Step 2: Generating expect functions...", flush=True)
    import shlex

    generate_expect_cmd = [
        *shlex.split(args.msfragger_cmd),
        "--generate_expect_functions",
        str(combined_histo.name),
    ]
    print(f"  Command: {' '.join(generate_expect_cmd)}", flush=True)
    subprocess.run(generate_expect_cmd, cwd=str(outdir), check=True)

    # Verify expect score file was created
    expect_tsv = outdir / f"{args.sample_name}_expectscore.tsv"
    if not expect_tsv.exists():
        raise RuntimeError(
            f"Expected expect score file not found: {expect_tsv}. "
            f"MSFragger --generate_expect_functions may have failed."
        )

    # Step 3: Merge pepXML and PIN files
    print("Step 3: Merging pepXML and PIN files...", flush=True)
    combine_results(
        sample_name=args.sample_name,
        chunk_dirs=chunk_dirs,
        outdir=outdir,
        fasta_path=args.fasta_path,
        output_report_topN=args.output_report_topN,
        output_max_expect=args.output_max_expect,
        output_file_extension=args.output_file_extension,
    )

    print(f"Merge complete. Results in: {outdir}", flush=True)


if __name__ == "__main__":
    main()
