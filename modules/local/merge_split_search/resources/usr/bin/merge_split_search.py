def write_combined_scores_histo(
    sample_name: str,
    chunk_dirs: typing.List[pathlib.Path],
    outdir: pathlib.Path,
) -> pathlib.Path:
    """
    Sum score histograms across all chunks for a sample.

    Ports msfragger_pep_split.py lines 157-163.

    Args:
        sample_name: Sample stem name (e.g., "sample_001")
        chunk_dirs: List of chunk directories containing per-chunk histogram TSVs
        outdir: Output directory for the combined histogram

    Returns:
        Path to the combined histogram TSV file
    """
    histo_filename = f"{sample_name}_scores_histogram.tsv"
    scores_histo = sum(
        np.loadtxt(chunk_dir / histo_filename, dtype=np.uint64, delimiter="\t")
        for chunk_dir in chunk_dirs
    )
    combined_path = outdir / histo_filename
    np.savetxt(combined_path, scores_histo, delimiter="\t", fmt="%d")
    return combined_path


def get_expect_functions(
    expect_tsv_path: pathlib.Path,
) -> typing.List[typing.Callable[[float], float]]:
    """
    Load expect function lookup tables from TSV generated by MSFragger
    --generate_expect_functions.

    Ports msfragger_pep_split.py lines 167-175.

    Args:
        expect_tsv_path: Path to the expectscore TSV file

    Returns:
        List of expect functions, one per spectrum
    """
    expect_table = np.loadtxt(expect_tsv_path, ndmin=2)

    def expect_function(row):
        m_fA0, m_fA1, m_dLimit = row

        def expect(under_f: float) -> float:
            dR: float = np.power(10, m_fA0 + m_fA1 * under_f)
            return max(dR, m_dLimit)

        return expect

    return list(map(expect_function, expect_table))


def get_spectrum(p: pathlib.Path) -> typing.Iterator[bytes]:
    """
    Iterator to extract spectrum_query blocks from a pepXML file.

    Ports msfragger_pep_split.py lines 177-195.
    Uses mmap for memory-efficient access to large files.

    Args:
        p: Path to pepXML file

    Yields:
        Raw bytes of each spectrum_query block, or b'' for missing indices
    """
    rec = re.compile(
        b'^<spectrum_query .+? index="(.+?)" (?s:.+?)^</spectrum_query>', re.MULTILINE
    )
    with p.open("rb") as f:
        mm = mmap.mmap(f.fileno(), 0, access=mmap.ACCESS_READ)
        last_idx = 0
        it = rec.finditer(mm)
        e = next(it, None)
        while True:
            last_idx += 1
            if e is None:
                while True:
                    yield b""  # end of file
            elif int(e.group(1)) == last_idx:
                yield e.group()
                e = next(it, None)
            else:
                yield b""


def step1(
    spec_queries: typing.Tuple[bytes, ...],
) -> typing.Tuple[typing.List[bytes], typing.List[typing.Tuple[int, bytes]]]:
    """
    Parse spectrum_query headers and search_hit elements from all chunks.

    Ports msfragger_pep_split.py lines 203-218.
    Extracts all search hits, deduplicates by header template (hit_rank and protein
    replaced with {}), keeping first occurrence.

    Args:
        spec_queries: Tuple of raw spectrum_query bytes, one per chunk

    Returns:
        Tuple of (spectrum_query_headers, deduplicated_search_hits)
        where search_hits is list of (part_index, search_hit_bytes)
    """
    spec_query_heads = [
        (spectrum_query.splitlines()[0] if len(spectrum_query) > 0 else spectrum_query)
        for spectrum_query in spec_queries
    ]
    search_hits = [
        (part, e)
        for part, spectrum_query in enumerate(spec_queries)
        for e in re_search_hit.findall(spectrum_query)
    ]
    search_hit_start_tags = [
        re_search_hit_first_line.sub(b"{}", search_hit.splitlines()[0])
        for _, search_hit in search_hits
    ]
    header_txts = [
        (search_hit_tag, search_hit)
        for search_hit_tag, search_hit in zip(search_hit_start_tags, search_hits)
    ]
    d: typing.Dict[bytes, typing.Tuple[int, bytes]] = collections.defaultdict(list)
    for header, txt in header_txts:
        d[header].append(txt)
    for k, v in d.items():
        d[k] = v[0]
    return spec_query_heads, list(d.values())


def get_prots_from_search_hit(sh: bytes) -> typing.List[Prot]:
    """
    Extract protein information from a search_hit XML element.

    Ports msfragger_pep_split.py lines 343-350.

    Args:
        sh: Raw search_hit XML bytes

    Returns:
        List of Prot namedtuples extracted from the search_hit
    """
    fields = [
        sh_protein_descr.findall(sh),
        sh_protein.findall(sh),
        sh_peptide_prev_aa.findall(sh),
        sh_peptide_next_aa.findall(sh),
        sh_num_tol_term.findall(sh),
    ]
    assert len(set(map(len, fields))) == 1
    return list(map(Prot._make, zip(*fields)))


def replace_prot_list(sh: bytes, prot_list: typing.List[Prot]) -> bytes:
    """
    Replace protein list in search_hit XML with the full merged protein list.

    Ports msfragger_pep_split.py lines 368-385.
    When a peptide maps to more proteins across chunks than in a single chunk,
    updates the primary protein and adds alternative_protein elements.

    Args:
        sh: Raw search_hit XML bytes
        prot_list: Complete list of Prot entries from all chunks

    Returns:
        Updated search_hit XML bytes with full protein list
    """
    prot_list_original = get_prots_from_search_hit(sh)
    if len(prot_list) == len(prot_list_original):
        return sh
    if len(prot_list) < len(prot_list_original):
        raise RuntimeError(
            f"Merged protein list ({len(prot_list)}) smaller than original "
            f"({len(prot_list_original)})"
        )
    sh = sh_protein_descr.sub(
        b'protein_descr="' + prot_list[0].protein_descr.replace(b"\\", rb"\\") + b'"',
        sh,
        count=1,
    )
    sh = sh_protein.sub(b'protein="' + prot_list[0].protein + b'"', sh, count=1)
    sh = sh_peptide_prev_aa.sub(
        b'peptide_prev_aa="' + prot_list[0].peptide_prev_aa + b'"', sh, count=1
    )
    sh = sh_peptide_next_aa.sub(
        b'peptide_next_aa="' + prot_list[0].peptide_next_aa + b'"', sh, count=1
    )
    sh = sh_num_tol_term.sub(
        b'num_tol_term="' + prot_list[0].num_tol_term + b'"', sh, count=1
    )
    altprot = "\n".join(
        f'<alternative_protein protein_descr="{prot.protein_descr.decode()}" '
        f'protein="{prot.protein.decode()}" '
        f'peptide_prev_aa="{prot.peptide_prev_aa.decode()}" '
        f'peptide_next_aa="{prot.peptide_next_aa.decode()}" '
        f'num_tol_term="{prot.num_tol_term.decode()}"/>'
        for prot in prot_list[1:]
    )
    sh = re.compile(b"<alternative_protein.*(?:\n<alternative_protein.*)*").sub(b"", sh)
    sh = re.compile(b'num_tot_proteins="(.+?)"').sub(
        f'num_tot_proteins="{len(prot_list)}"'.encode(), sh
    )
    search_hit_tag, rest = sh.split(b"\n", 1)
    return search_hit_tag + b"\n" + altprot.encode() + b"\n" + rest


def get_pep_to_prot_mapping(
    pepxml_files: typing.List[pathlib.Path],
) -> typing.Mapping[bytes, typing.List[Prot]]:
    """
    Build peptide-to-protein mapping across all chunks' pepXML files.

    Ports msfragger_pep_split.py lines 352-366.
    Scans all pepXML files to collect every protein that each peptide maps to,
    deduplicates, and returns a frozen mapping.

    Args:
        pepxml_files: List of pepXML file paths from all chunks

    Returns:
        Mapping from peptide sequence bytes to sorted list of Prot entries
    """
    import types

    def make_dict_entry(sh):
        (key,) = sh_peptide.findall(sh)
        return key, get_prots_from_search_hit(sh)

    pep_to_prot: typing.Dict[bytes, typing.List[Prot]] = collections.defaultdict(list)
    for pepxml in pepxml_files:
        for sh in re_search_hit.findall(pepxml.read_bytes()):
            pep, prot = make_dict_entry(sh)
            pep_to_prot[pep].extend(prot)
    pep_to_prot.default_factory = None
    for k in pep_to_prot.keys():
        pep_to_prot[k] = sorted(set(pep_to_prot[k]))
    return types.MappingProxyType(pep_to_prot)


def get_pepxmls(
    sample_name: str,
    chunk_dirs: typing.List[pathlib.Path],
    output_file_extension: str = "pepXML",
) -> typing.Tuple[bool, typing.List[typing.List[pathlib.Path]]]:
    """
    Get pepXML paths from all chunks for a given sample.

    Ports msfragger_pep_split.py lines 368-399 (get_pepxmls + all_exists).
    Handles both ranked and unranked pepXML output formats.

    Args:
        sample_name: Sample stem name
        chunk_dirs: List of chunk directories
        output_file_extension: pepXML file extension (default: "pepXML")

    Returns:
        Tuple of (is_ranked, list_of_pepxml_path_lists)
        If ranked, inner lists are per-rank; if unranked, single inner list.
    """
    pepxml_parts_norank = [
        chunk_dir / f"{sample_name}.{output_file_extension}" for chunk_dir in chunk_dirs
    ]
    if all(p.exists() for p in pepxml_parts_norank):
        return False, [pepxml_parts_norank]

    # Check for ranked pepXMLs (_rank1, _rank2, ...)
    pepxml_parts_ranks = []
    for rank in itertools.count(1):
        ranked_paths = [
            chunk_dir / f"{sample_name}_rank{rank}.{output_file_extension}"
            for chunk_dir in chunk_dirs
        ]
        if all(p.exists() for p in ranked_paths):
            pepxml_parts_ranks.append(ranked_paths)
        else:
            break
    return True, pepxml_parts_ranks


def get_pepxml_header(p: pathlib.Path, fasta_path: str) -> bytes:
    """
    Extract pepXML header template from a chunk pepXML file.

    Ports msfragger_pep_split.py lines 289-303.
    Replaces the date and summary_xml attributes with format placeholders (%b)
    so that the merged output can fill in new values.

    Args:
        p: Path to a pepXML file
        fasta_path: Original FASTA path string to substitute for chunk FASTA name

    Returns:
        Header bytes template with %b placeholders for date and summary_xml
    """
    with p.open("rb") as f:
        mm = mmap.mmap(f.fileno(), 0, access=mmap.ACCESS_READ)
        try:
            ret = re_pepxml_header.match(mm).group()
        except AttributeError:
            print(f"Failed to parse pepXML header from: {p}", file=sys.stderr)
            raise
        # Replace date and summary_xml with format placeholders
        ret0 = re.compile(b'date="(.+?)"').sub(b'date="%b"', ret.replace(b"%", b"%%"))
        # Replace summary_xml path with placeholder
        header = re.compile(b'summary_xml="(.+?)"').sub(b'summary_xml="%b"', ret0)

        # Replace chunk FASTA name with full FASTA path
        # The chunk pepXML references the chunk's local FASTA; replace with original
        chunk_fasta_name = p.parent / "database.fasta"
        if chunk_fasta_name.exists():
            header = header.replace(
                f'"{chunk_fasta_name.name}"'.encode(),
                f'"{fasta_path}"'.encode(),
            )
        return header


def new_spec(
    expect_func: typing.Callable[[float], float],
    spectrum_query_parts: typing.Tuple[bytes, ...],
    pep_to_prot: typing.Mapping[bytes, typing.List[Prot]],
    output_report_topN: int,
    output_max_expect: float,
) -> typing.List[bytes]:
    """
    Per-spectrum merge: collect hits from all chunks, sort, keep topN, re-rank.

    Ports msfragger_pep_split.py lines 249-287.
    For each spectrum, collects search_hits from ALL chunks, sorts by
    (expectscore, 1/hyperscore, abs(massdiff)), keeps topN hits that pass
    the expect threshold, re-ranks, and reconciles protein lists.

    Args:
        expect_func: Function mapping hyperscore to expect value
        spectrum_query_parts: Raw spectrum_query bytes from each chunk
        pep_to_prot: Peptide-to-protein mapping from all chunks
        output_report_topN: Maximum number of hits to report per spectrum
        output_max_expect: Maximum expect value threshold

    Returns:
        List of bytes fragments forming the merged spectrum_query XML
    """
    if set(spectrum_query_parts) == {b""}:
        return [b""]

    spectrum_query_headers, search_hits = step1(spectrum_query_parts)

    def get_massdiff_and_scores(search_hit: bytes):
        match = re_massdiff_scores.search(search_hit)
        if match is None:
            print(search_hit, file=sys.stderr)
            raise RuntimeError("Failed to parse massdiff/scores from search_hit")
        massdiff, hyperscore, nextscore = [float(e) for e in match.groups()]
        return massdiff, hyperscore, nextscore, expect_func(hyperscore)

    def step2(
        search_hits: typing.List[typing.Tuple[int, bytes]],
    ) -> typing.List[bytes]:
        search_hit_with_massdiff_and_scores = (
            specinfo._make(get_massdiff_and_scores(search_hit) + (part, search_hit))
            for part, search_hit in search_hits
        )
        # Sort by expectscore, then 1/hyperscore, then abs(massdiff)
        sorted_search_hits0 = sorted(
            search_hit_with_massdiff_and_scores,
            key=lambda x: (x.expectscore, 1 / x.hyperscore, abs(x.massdiff)),
        )[:output_report_topN]
        sorted_search_hits1 = list(
            itertools.takewhile(
                lambda x: x.expectscore <= output_max_expect, sorted_search_hits0
            )
        )
        if len(sorted_search_hits1) == 0:
            return [b""]

        # Compute new nextscores: each hit's nextscore = next hit's hyperscore
        new_nextscores = [e.hyperscore for e in sorted_search_hits1][1:] + [
            min(sorted_search_hits1, key=lambda x: x.nextscore).nextscore
        ]
        sorted_search_hits = [
            specinfo._make(
                (None, s.hyperscore, new_nextscore, s.expectscore, s.part, s.xml)
            )
            for s, new_nextscore in zip(sorted_search_hits1, new_nextscores)
        ]

        def make_new_txt(
            search_hit: bytes,
            hit_rank: int,
            hyperscore: float,
            nextscore: float,
            expectscore: float,
        ) -> bytes:
            return re_update_search_hit.sub(
                f"\\g<1>{hit_rank}\\2\n"
                f'<search_score name="hyperscore" value="{hyperscore:.3f}"/>\n'
                f'<search_score name="nextscore" value="{nextscore:.3f}"/>\n'
                f'<search_score name="expect" value="{expectscore:.3e}"/>\n'
                f"\\3\n".encode(),
                search_hit,
            )

        def amend_prot_list(sh: bytes) -> bytes:
            (pep,) = sh_peptide.findall(sh)
            return replace_prot_list(sh, pep_to_prot[pep])

        b = [
            amend_prot_list(
                make_new_txt(s.xml, hit_rank, s.hyperscore, s.nextscore, s.expectscore)
            )
            for hit_rank, s in enumerate(sorted_search_hits, 1)
        ]
        return (
            [
                spectrum_query_headers[sorted_search_hits[0].part],
                b"\n<search_result>\n",
            ]
            + b
            + [b"</search_result>\n</spectrum_query>\n"]
        )

    return step2(search_hits)


def write_pepxml_single_rank(
    outfile: pathlib.Path,
    pepxml_parts: typing.List[pathlib.Path],
    expect_funcs: typing.List[typing.Callable],
    pep_to_prot: typing.Mapping[bytes, typing.List[Prot]],
    fasta_path: str,
    output_report_topN: int,
    output_max_expect: float,
) -> None:
    """
    Write a single merged pepXML file from chunk pepXML parts.

    Ports msfragger_pep_split.py lines 401-411.
    Iterates through all spectra in lock-step across chunks, merges hits per
    spectrum, and writes the unified pepXML.

    Args:
        outfile: Output pepXML path
        pepxml_parts: List of chunk pepXML paths (one per chunk)
        expect_funcs: List of expect functions (one per spectrum)
        pep_to_prot: Peptide-to-protein mapping
        fasta_path: Original FASTA path for header substitution
        output_report_topN: Maximum hits per spectrum
        output_max_expect: Maximum expect value
    """
    zip_spec_pos = zip(*(get_spectrum(e) for e in pepxml_parts))
    pepxml_header_set = set(get_pepxml_header(e, fasta_path) for e in pepxml_parts)
    pepxml_header = next(iter(pepxml_header_set))
    with pathlib.Path(outfile).open("wb") as f:
        f.write(
            pepxml_header
            % (
                datetime.datetime.now().strftime("%Y-%m-%dT%H:%M:%S").encode(),
                os.fspath(outfile).encode(),
            )
        )
        f.write(b"\n")
        for i, (expect_func, spectrum_query_parts) in enumerate(
            zip(expect_funcs, zip_spec_pos)
        ):
            f.writelines(
                new_spec(
                    expect_func,
                    spectrum_query_parts,
                    pep_to_prot,
                    output_report_topN,
                    output_max_expect,
                )
            )
            if i % (1 << 14) == 0:
                print(f"Writing: {outfile.stem}\tspectrum: {i}", flush=True)
        f.write(b"</msms_run_summary>\n</msms_pipeline_analysis>\n")


def write_pepxml(
    sample_name: str,
    chunk_dirs: typing.List[pathlib.Path],
    outdir: pathlib.Path,
    fasta_path: str,
    output_report_topN: int,
    output_max_expect: float,
    output_file_extension: str = "pepXML",
) -> None:
    """
    Merge pepXML files for a sample across all chunks.

    Ports msfragger_pep_split.py lines 388-398 (write_pepxml).
    Handles both ranked and unranked pepXML formats.

    Args:
        sample_name: Sample stem name
        chunk_dirs: List of chunk directories
        outdir: Output directory for merged files
        fasta_path: Original FASTA path for header substitution
        output_report_topN: Maximum hits per spectrum
        output_max_expect: Maximum expect value
        output_file_extension: pepXML file extension
    """
    ranked, all_pepxmls = get_pepxmls(sample_name, chunk_dirs, output_file_extension)

    # Build peptide-to-protein mapping from all chunk pepXMLs
    all_pepxml_files = list(itertools.chain.from_iterable(all_pepxmls))
    pep_to_prot = get_pep_to_prot_mapping(all_pepxml_files)

    # Load expect functions from the combined histogram
    expect_tsv = outdir / f"{sample_name}_expectscore.tsv"
    expect_funcs = get_expect_functions(expect_tsv)

    if ranked:
        for rank, pepxml_parts in enumerate(all_pepxmls, 1):
            write_pepxml_single_rank(
                outdir / f"{sample_name}_rank{rank}.{output_file_extension}",
                pepxml_parts,
                expect_funcs,
                pep_to_prot,
                fasta_path,
                output_report_topN,
                output_max_expect,
            )
    else:
        write_pepxml_single_rank(
            outdir / f"{sample_name}.{output_file_extension}",
            all_pepxmls[0],
            expect_funcs,
            pep_to_prot,
            fasta_path,
            output_report_topN,
            output_max_expect,
        )


def read_pin(p: pathlib.Path) -> typing.Dict[str, typing.List[typing.List[str]]]:
    """
    Read a PIN file into a dict keyed by spectrum identifier.

    Ports msfragger_pep_split.py lines 469-480.
    Groups rows by spectrum ID (SpecId column with trailing charge stripped).

    Args:
        p: Path to PIN file

    Returns:
        Dict mapping spectrum_id (without charge suffix) to list of rows
    """
    csv.field_size_limit(2**31 - 1)
    with p.open() as f:
        reader = csv.reader(f, csv.excel_tab)
        header = next(reader)
        specId_idx = header.index("SpecId")
        d: typing.Dict[str, typing.List[typing.List[str]]] = collections.defaultdict(
            list
        )
        for row in reader:
            d[row[specId_idx].rsplit(".", 1)[0]].append(row)
    d.default_factory = None
    return d


def write_pin(
    sample_name: str,
    chunk_dirs: typing.List[pathlib.Path],
    outdir: pathlib.Path,
    output_report_topN: int,
    output_file_extension: str = "pepXML",
) -> None:
    """
    Merge PIN files for a sample across all chunks.

    Ports msfragger_pep_split.py lines 413-467.
    Groups hits per spectrum, sorts by hyperscore descending, re-ranks,
    recalculates log10_evalue and delta_hyperscore, and reconciles alt proteins.

    Args:
        sample_name: Sample stem name
        chunk_dirs: List of chunk directories
        outdir: Output directory for merged PIN file
        output_report_topN: Maximum hits per spectrum
        output_file_extension: pepXML file extension (for spectrum index lookup)
    """
    pin_files = [chunk_dir / f"{sample_name}.pin" for chunk_dir in chunk_dirs]
    if not all(p.exists() for p in pin_files):
        return

    header = pin_files[0].read_text().splitlines()[0].split("\t")
    proteins_idx = header.index("Proteins")
    peptide_idx = header.index("Peptide")
    rank_idx = header.index("rank")
    hyperscore_idx = header.index("hyperscore")
    log10_evalue_idx = (
        header.index("log10_evalue") if "log10_evalue" in header else None
    )
    delta_hyperscore_idx = (
        header.index("delta_hyperscore") if "delta_hyperscore" in header else None
    )
    abs_ppm_idx = header.index("abs_ppm") if "abs_ppm" in header else None

    pins = [read_pin(p) for p in pin_files]

    # Build spectrum-to-index mapping from pepXMLs
    ranked, all_pepxmls = get_pepxmls(sample_name, chunk_dirs, output_file_extension)
    spec_to_index_map = {}
    for pepxml_path in itertools.chain.from_iterable(all_pepxmls):
        text = pepxml_path.read_text()
        for _charge, spec, idx in re.compile(
            '<spectrum_query .*assumed_charge="(\\d+?)" '
            '.*spectrum="(.+?)" '
            '.*index="(\\d+?)"'
        ).findall(text):
            spec_key = spec[: spec.rindex(".")]
            spec_to_index_map[spec_key] = int(idx)

    # Merge PIN entries across chunks
    d: typing.Dict[str, typing.List[typing.List[str]]] = collections.defaultdict(list)
    for pin in pins:
        for k, v in pin.items():
            d[k].extend(v)
    d.default_factory = None

    # Sort spectra by index, hits within spectrum by hyperscore descending
    sorted_spectrums = sorted(
        [
            (
                spec_to_index_map[k],
                sorted(
                    [
                        (
                            float(e[hyperscore_idx]),
                            None if abs_ppm_idx is None else -float(e[abs_ppm_idx]),
                            e,
                        )
                        for e in v
                    ],
                    reverse=True,
                ),
            )
            for k, v in d.items()
        ]
    )
    del pins, d

    # Recalculate log10_evalue using expect functions if present
    expect_funcs = (
        None
        if log10_evalue_idx is None
        else get_expect_functions(outdir / f"{sample_name}_expectscore.tsv")
    )

    # First pass: update delta_hyperscore, log10_evalue, rank, and collect alt proteins
    pep_alt_prot: typing.Dict[str, typing.Set[str]] = collections.defaultdict(set)
    for index, hits in sorted_spectrums:
        if delta_hyperscore_idx is not None:
            for h1, h2 in zip(hits, hits[1:]):
                delta_hyperscore = float(h1[0]) - float(h2[0])
                h1[2][delta_hyperscore_idx] = str(delta_hyperscore)
        for rank, (hyperscore, _, row) in enumerate(hits, 1):
            if log10_evalue_idx is not None:
                row[log10_evalue_idx] = str(
                    np.log10(expect_funcs[index - 1](hyperscore))
                )
            row[rank_idx] = str(rank)
            row[0] = row[0].rsplit("_", 1)[0] + "_" + str(rank)
            pep_alt_prot[row[peptide_idx]] |= set(row[proteins_idx:]) - {""}
    pep_alt_prot.default_factory = None

    # Second pass: reconcile alternative proteins
    for index, hits in sorted_spectrums:
        for rank, (hyperscore, _, row) in enumerate(hits, 1):
            alt_prots = pep_alt_prot[row[peptide_idx]]
            l1, l2 = len(set(row[proteins_idx:]) - {""}), len(alt_prots)
            if l1 != l2:
                row[proteins_idx:] = sorted(alt_prots)

    # Write merged PIN file
    outfile = outdir / f"{sample_name}.pin"
    with pathlib.Path(outfile).open("w") as f:
        f.write("\t".join(header) + "\n")
        for _, hits in sorted_spectrums:
            for hit in hits[:output_report_topN]:
                f.write("\t".join(hit[2]) + "\n")


def combine_results(
    sample_name: str,
    chunk_dirs: typing.List[pathlib.Path],
    outdir: pathlib.Path,
    fasta_path: str,
    output_report_topN: int,
    output_max_expect: float,
    output_file_extension: str = "pepXML",
) -> None:
    """
    Parallel pepXML + PIN merging for a single sample.

    Ports msfragger_pep_split.py lines 488-496.
    Uses ProcessPoolExecutor for parallel execution of pepXML and PIN merging.

    Args:
        sample_name: Sample stem name
        chunk_dirs: List of chunk directories
        outdir: Output directory for merged results
        fasta_path: Original FASTA path
        output_report_topN: Maximum hits per spectrum
        output_max_expect: Maximum expect value
        output_file_extension: pepXML file extension
    """
    with concurrent.futures.ProcessPoolExecutor(max_workers=2) as exe:
        fs1 = exe.submit(
            write_pepxml,
            sample_name,
            chunk_dirs,
            outdir,
            fasta_path,
            output_report_topN,
            output_max_expect,
            output_file_extension,
        )
        fs2 = exe.submit(
            write_pin,
            sample_name,
            chunk_dirs,
            outdir,
            output_report_topN,
            output_file_extension,
        )
    for f in [fs1, fs2]:
        f.result()


