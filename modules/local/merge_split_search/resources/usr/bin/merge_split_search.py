#!/usr/bin/env python3
"""
merge_split_search.py: Merge MSFragger split-database search results per sample.

Faithfully ports the merge logic from FragPipe's msfragger_pep_split.py (lines 157-496).
For each sample, collects pepXML and PIN results from all database chunks, merges them
by re-ranking hits per spectrum, and writes unified output files.

Usage:
    merge_split_search.py \\
        --sample_name sample_001 \\
        --chunk_dirs chunk_0,chunk_1,chunk_2 \\
        --num_chunks 3 \\
        --msfragger_cmd msfragger \\
        --outdir merged \\
        --output_report_topN 1 \\
        --output_max_expect 50.0
"""

